package com.loiane.cursojava.aula35.lab.Exercicio04;

public class ConversaoDeUnidadesDeArea {
	
	public static double MetroQuaPorPesQua(double valor) {
		return valor * 10.76;
	}
	
	public static double PeQuaPorCenQua(double valor) {
		return valor * 929;
	}
	
	public static double MiQuaPorAcre(double valor) {
		return valor * 640;
	}
	
	public static double AcrePorPeQua(double valor) {
		return valor * 43560;
	}

}
