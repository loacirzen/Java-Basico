package com.loiane.cursojava.aula29;

public class Carro2 {
	
	String marca;
	String modelo;
	int numPassageiros;
	double capCombustivel;
	double consumoCombustive;
	
	public Carro2(String marca, String modelo, int numPassageiros, double capCombustivel, double consumoCombustive) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.numPassageiros = numPassageiros;
		this.capCombustivel = capCombustivel;
		this.consumoCombustive = consumoCombustive;
	}

	public Carro2() {}
	
	

}
