package com.loiane.cursojava.aula35.lab.Exercicio05;

public class ConversaoDeUnidadeDeVolume {
	
	public static double litroPorCentCubi(double valor) {
		return valor * 1000;
	}
	
	public static double metroCubiPorLitro(double valor) {
		return valor * 1000;
	}
	
	public static double metroCubiPorPesCub(double valor) {
		return valor * 35.32;
	}
	
	public static double galaoAmeriPorPoleCubi(double valor) {
		return valor * 231;
	}
	
	public static double galaoAmeriPorLitros(double valor) {
		return valor * 3.785;
	}
}
