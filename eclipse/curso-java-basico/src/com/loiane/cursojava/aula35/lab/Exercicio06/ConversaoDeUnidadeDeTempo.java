package com.loiane.cursojava.aula35.lab.Exercicio06;

public class ConversaoDeUnidadeDeTempo {
	
	public static int minutosPorSegundos(int valor) {
		return valor * 60;
	}
	
	public static int horaPorMinutos(int valor) {
		return valor * 60;
	}
	
	public static int diaPorHora(int valor) {
		return valor * 24;
	}
	
	public static int semanaPorDias(int valor) {
		return valor * 7;
	}
	
	public static int mesPorDias(int valor) {
		return valor * 30;
	}
	
	public static double anosPorDias(int valor) {
		return valor * 365.25;
	}
		
}
